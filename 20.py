valor = int(input("Digite um número (1-7): "))

if valor == 1:
    print("Segunda-Feira")
elif valor == 2:
    print("Terça-Feira")
elif valor == 3:
    print("Quarta-Feira")
elif valor == 4:
    print("Quinta-Feira")
elif valor == 5:
    print("Sexta-Feira")
elif valor == 6:
    print("Sábadera")
elif valor == 7:
    print("Domingou")

else:
    print("O Programa considera somente valores de 1 a 7")


